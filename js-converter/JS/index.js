
function fahrenheitCelsius(){
    let fahrenheit = 120; // variabelen fahrenheit

    let kult = (fahrenheit - 32) * 5 /9; // formel for fahrenheit til celcius

    console.log(kult);
}

fahrenheitCelsius();


function celciusFahrenheit(){
    let celcius = 26;

    let ikkekult = (celcius * 1.8) + 32;

    console.log(ikkekult);
}

celciusFahrenheit();





















